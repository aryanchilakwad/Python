"""
Q1: Take two numbers as input from the user. Print their sum, difference,
product, and remainder.
"""

num1 = int(input("Enter your number1 = "))
num2 = int(input("Enter your number2 = "))

print(f"The sum of two numbers = {num1 + num2} ")
print(f"The difference of two numbers = {num1 - num2} ")
print(f"The product of two numbers = {num1 * num2} ")
print(f"The remainder of two numbers = {num1 % num2} ")