'''
Create list in python
'''

list1 = [1,2,3,4,5]
print(list1)

list2 = ['a','b','c','d','e']
print(list2)

list3 = [1,'a',2,'b',3,'c']
print(list3) #List can contain different data types

'''Operations in list'''

result = list1 + list2
print(result) #Concatenation of two lists

result2 = list1 * list2
print(result2) #Repetition of list1 by the number of elements in list2

